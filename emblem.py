# (color1, color2, blend)
# blend:
# - 0: color1 100%
# - 1: color1 75% : color2 25%
# - 2: color1 50% : color2 50%
# - 3: color1 25% : color2 75%
# - 4: color2 100%
colors = [
    [
        [(69,69,0),   (69,69,0),   (69,69,0)],
        [(69,69,0),   (69,69,0),   (69,69,0)],
        [(69,104,1),  (69,104,2),  (69,104,3)],
    ], [
        [(69,69,0),   (69,69,0),   (69,69,0)],
        [(69,69,0),   (69,69,0),   (69,104,1)],
        [(104,139,0), (104,139,1), (104,139,2)],
    ], [
        [(69,69,0),   (69,69,0),   (69,69,0)],
        [(69,69,0),   (69,104,1),  (69,104,2)],
        [(104,139,2), (104,139,3), (104,139,4)],
    ], [
        [(69,104,1),  (69,104,2),  (69,104,3)],
        [(139,139,0), (139,139,0), (139,139,0)],
        [(139,169,0), (139,169,1), (139,169,2)],
    ], [
        [(104,139,0), (104,139,1), (104,139,2)],
        [(139,169,1), (139,169,2), (139,169,3)],
        [(169,168,2), (169,203,1), (169,203,2)],
    ], [
        [(139,169,2), (139,169,3), (139,169,4)],
        [(169,168,1), (169,203,1), (169,203,2)],
        [(168,203,2), (168,203,3), (168,203,4)],
    ], [
        [(169,168,1), (169,168,2), (169,168,3)],
        [(168,203,1), (168,203,2), (168,203,3)],
        [(203,203,0), (203,203,0), (203,203,0)],
    ]
]

for i, row in enumerate(colors):
    for j, cell in enumerate(row):
        for c1, c2, blend in cell:
            if (i, j) == (3, 1):
                assert blend == 0
                print('\033[38;5;%dm▄' % c1, end='')
                continue

            if blend == 0:
                print('\033[48;5;%dm ' % c1, end='')
            elif blend == 1:
                print('\033[48;5;%dm\033[38;5;%dm░' % (c1, c2), end='')
            elif blend == 2:
                print('\033[48;5;%dm\033[38;5;%dm▒' % (c1, c2), end='')
            elif blend == 3:
                print('\033[48;5;%dm\033[38;5;%dm▓' % (c1, c2), end='')
            elif blend == 4:
                print('\033[48;5;%dm ' % c2, end='')

        print('\033[0m ', end='')
    print()
